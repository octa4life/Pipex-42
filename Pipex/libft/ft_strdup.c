#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	char	*str;
	int		i;

	i = 0;
	len = ft_strlen(s);
	str = malloc(len + 1);
	if (str == NULL)
		return (NULL);
	while (i < len)
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
